package com.jvv.p2p.entity;

public class ExtUserDto {
    private String id;

    private String channelId;

    private String userId;

    private String phone;

    private String password;

    private String activity;

    private String realnameopen;

    private String payopen;

    private String bindopen;

    private String extUserId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }

    public String getRealnameopen() {
        return realnameopen;
    }

    public void setRealnameopen(String realnameopen) {
        this.realnameopen = realnameopen;
    }

    public String getPayopen() {
        return payopen;
    }

    public void setPayopen(String payopen) {
        this.payopen = payopen;
    }

    public String getBindopen() {
        return bindopen;
    }

    public void setBindopen(String bindopen) {
        this.bindopen = bindopen;
    }

    public String getExtUserId() {
        return extUserId;
    }

    public void setExtUserId(String extUserId) {
        this.extUserId = extUserId;
    }
}